sprites.onOverlap(SpriteKind.Player, SpriteKind.Projectile, function (sprite, otherSprite) {
    if (invnce == 0) {
        end = 1
        animation.setAction(raptor, ActionKind.Dead)
        pause(50)
        game.over(false, effects.dissolve)
    }
})
sprites.onOverlap(SpriteKind.Projectile, SpriteKind.powerup, function (sprite, otherSprite) {
    otherSprite.destroy()
})
sprites.onOverlap(SpriteKind.Player, SpriteKind.powerup, function (sprite, otherSprite) {
    invnce = 1
    game.splash("Invincible!")
    otherSprite.startEffect(effects.smiles)
    otherSprite.destroy()
    timer.after(10000, function () {
        invnce = 0
        game.splash("Invincibility over!")
        sprite.startEffect(effects.halo)
        timer.after(500, function () {
            effects.clearParticles(sprite)
        })
    })
})
